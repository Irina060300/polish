char **init_pole() {
    char **pole = (char **)malloc(sizeof(char *) * POLE_HEIGHT);
    for (int i = 0; i < POLE_HEIGHT; i++) {
        pole[i] = (char *)malloc(sizeof(char) * POLE_WIDTH);
    }
    return pole;
}

